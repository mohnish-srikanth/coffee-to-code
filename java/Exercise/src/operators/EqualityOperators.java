package operators;

public class EqualityOperators {
	
	public static void main(String[] args) {
	
	int a = 2, b = 5;
	
	System.out.println(a == b);
	System.out.println(a != b);
	System.out.println(a > b);
	System.out.println(a < b);
	System.out.println(a >= b);
	System.out.println(a <= b);
	
	
	}
}
