package operators;

public class Operators {

	public static void main(String[] args) {
		
		double num1 = 8.4, num2 = 5.6, result = 0.0;
		result = num1 + num2;
		System.out.println(result);
		
		result = num1 - num2;
		System.out.println(result);
		
		result = num1 * num2;
		System.out.println(result);
		
		result = num1 / num2;
		System.out.println(result);
		
		result = num1 % num2;
		System.out.println(result);
		
	}

}
