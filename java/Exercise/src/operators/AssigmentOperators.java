package operators;

public class AssigmentOperators {
	
	public static void main(String[] args) {
	
	int x = 10,  y = 20, z = 0;
	
	z = x + y;
	System.out.println(z);
	
	z += x; // z = z + x
	System.out.println(z);
	
	z -= x; // z = z - x
	System.out.println(z);
	
	z *= x; // z = z * x
	System.out.println(z);
	
	z /= x; // z = z / x
	System.out.println(z);
	
	z %= x; // z = z % x
	System.out.println(z);
	
	}
}
