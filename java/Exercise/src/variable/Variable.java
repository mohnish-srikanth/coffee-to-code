package variable;

public class Variable {

	public static void main(String[] args) {
		
		int a = 10, b = 20; // this is a more convenient declaration
//		int b = 20;
		int c = a + b;
		System.out.println("Result = " + c); // this will print "Result = 30"
		System.out.println("Result = " + a + b); // this will print "Result = 1020"
		System.out.println("Result = " + (a + b)); // this will print "Result = 30"
		
		
	}

}
