package datatype;

public class DataType {

	public static void main(String[] args) {
		
		boolean a = true;
		char b = 'R';
		byte c = 12; // -128 to 127
		short d = -356; //-32767 to 32767
		int e = 4358564;
		long f = -14751397413L; // for long values and L is the suffix for it
		float g = 5.123134688134F; // F is the suffix for float type
		double h = 7.21836124981241827; // large values with double precision
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);

	}

}
